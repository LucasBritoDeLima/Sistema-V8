    public void listar() {
        UsuarioDAO dao = new UsuarioDAO();
        List<Usuario> lista = dao.listarUsuario();
        DefaultTableModel dados = (DefaultTableModel) tabelaUsers.getModel();
        dados.setNumRows(0);

        for (Usuario u : lista) {
            dados.addRow(new Object[]{
                u.getIdUser(),
                u.getUser(),
                u.getSenha(),
                u.getNivel_acesso(),
                u.getSituacao()
            });
        }

    }

