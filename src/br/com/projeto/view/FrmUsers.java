    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        // Botão cadastrar cliente
        if (txtUsuario.getText().isEmpty() || txtSenha.getText().isEmpty() || txtCSenha.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Preencha os campos em branco");
        } else if (txtSenha.getText().equals(txtCSenha.getText())) {
            Usuario u = new Usuario();
            u.setUser(txtUsuario.getText());
            u.setSenha(txtSenha.getText());
            u.setNivel_acesso(cbNivel.getModel().getSelectedItem().toString());
            UsuarioDAO dao = new UsuarioDAO();
            dao.cadastrarUsuario(u);

            txtUsuario.setText(null);
            txtSenha.setText(null);
            txtCSenha.setText(null);
        } else {
            JOptionPane.showMessageDialog(null, "As senhas não estão iguais por favor confira as senhas digitadas");
        }
    }//GEN-LAST:event_btnAddActionPerformed

