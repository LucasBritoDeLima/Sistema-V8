    public void listar() {
        ClienteDAO dao = new ClienteDAO();
        List<Cliente> lista = dao.listarCliente();
        DefaultTableModel dados = (DefaultTableModel) tabelaCliente.getModel();
        dados.setNumRows(0);

        for (Cliente c : lista) {
            dados.addRow(new Object[]{
                c.getNome(),
                c.getCpf(),
                c.getRg(),
                c.getTel(),
                c.getTel2(),
                c.getLogradouro(),
                c.getNumero(),
                c.getBairro(),
                c.getCep(),
                c.getEstado(),
                c.getCidade(),
                c.getIdCliente()
            });
        }
    }

