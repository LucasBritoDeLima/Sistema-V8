    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
        // Botão editar motor
        if (txtGarantia.getText().isEmpty() || txtMotor.getText().isEmpty() || txtTipo.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Preencha os campos em branco");
        } else {
            Motor obj = new Motor();
            obj.setMotor(txtMotor.getText());
            obj.setTipo(txtTipo.getText());
            obj.setGarantia(txtGarantia.getText());
            obj.setCilindros(cbCilindro.getModel().getSelectedItem().toString());
            obj.setComb(cbComb.getModel().getSelectedItem().toString());
            obj.setIdMotor(Integer.parseInt(txtId.getText()));
            Cliente c = new Cliente();
            c = (Cliente) cbCli.getSelectedItem();
            obj.setCliente(c);
            obj.setTb_cliente_idCliente(c.getIdCliente());
            MotorDAO dao = new MotorDAO();
            dao.editarMotor(obj);
            txtMotor.setText(null);
            txtGarantia.setText(null);
            txtTipo.setText(null);

        }
    }//GEN-LAST:event_btnEditarActionPerformed

    private void btnApagarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnApagarActionPerformed
        // Inativar o motor
        int confirmacao = JOptionPane.showConfirmDialog(null, "Tem certeza que deseja eliminar este registro?");
        if (confirmacao == 0) {
            Motor obj = new Motor();
            obj.setIdMotor(Integer.parseInt(txtId.getText()));
            MotorDAO dao = new MotorDAO();
            dao.inativarMotor(obj);
            txtMotor.setText(null);
            txtGarantia.setText(null);
            txtTipo.setText(null);
        }
    }//GEN-LAST:event_btnApagarActionPerformed

