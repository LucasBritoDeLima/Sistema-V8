    public void listar() {
        MotorDAO dao = new MotorDAO();
        List<Motor> lista = dao.listarMotor();
        DefaultTableModel dados = (DefaultTableModel) tabelaMotor.getModel();
        dados.setNumRows(0);

        for (Motor m : lista) {
            dados.addRow(new Object[]{
                m.getMotor(),
                m.getTipo(),
                m.getCilindros(),
                m.getComb(),
                m.getGarantia(),
                m.getCliente().getNome(),
                m.getIdMotor()
            });
        }

    }

